class Product {
  constructor(
    id,
    ownerId,
    title,
    imageUrl,
    bgColor,
    sizes,
    description,
    price
  ) {
    this.id = id;
    this.ownerId = ownerId;
    this.title = title;
    this.imageUrl = imageUrl;
    this.bgColor = bgColor;
    this.sizes = sizes;
    this.description = description;
    this.price = price;
  }
}
export default Product;
